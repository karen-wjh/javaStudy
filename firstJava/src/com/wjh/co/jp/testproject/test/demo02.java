package com.wjh.co.jp.testproject.test;

public class demo02 {
    public static void main(String[] args) {
        printCount (20);
        /*打印指定次数的单词
         * */
//        打印次数是int
    }
    public static void printCount(int num) {
        for (int i = 0; i < num; i++) {
            System.out.println("yeah" + (i+1));
        }
    }
}
